#include <iostream>
#include <string>

class Figure {
protected:
	int sides_count = 0;
	std::string name = "Фигура";

public:
	Figure(int sides_count, std::string name) {
		this->sides_count = sides_count;
		this->name = name;
	}

	Figure() { sides_count = 0; }

	int get_sides_count() // публичный метод get_sides_count в классе Figure
	{
		return sides_count;
	}

	std::string get_name() { return name; }
};

class Triangle : public Figure {
	int sides_count = 3;
	std::string name = "Треугольник";

public:
	Triangle() {}
	int get_sides_count() { return sides_count; }
	std::string get_name() { return name; }
};

class Quadrangle : public Figure {
	int sides_count = 4;
	std::string name = "Четырехугольник";

public:
	Quadrangle() {}
	int get_sides_count() { return sides_count; }
	std::string get_name() { return name; }
};

int main() {

	setlocale(0, "");
	Figure fig;
	Triangle tri;
	Quadrangle quad;
	std::cout << fig.get_name() << ": " << fig.get_sides_count() << std::endl;
	std::cout << tri.get_name() << ": " << tri.get_sides_count() << std::endl;
	std::cout << quad.get_name() << ": " << quad.get_sides_count() << std::endl;
}


